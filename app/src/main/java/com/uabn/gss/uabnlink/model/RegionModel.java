package com.uabn.gss.uabnlink.model;

public class RegionModel {

    String RegionId, RegionName, RegionStatus;

    public String getRegionId() {
        return RegionId;
    }
    public void setRegionId(String id) {
        RegionId = id;
    }

    public String getRegionName() {
        return RegionName;
    }
    public void setRegionName(String name) {
        RegionName = name;
    }

    public String getRegionStatus() {
        return RegionStatus;
    }
    public void setRegionStatus(String status) {
        RegionStatus = status;
    }

}
