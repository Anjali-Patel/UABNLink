package com.uabn.gss.uabnlink.model;

public class CountryModel {

    String CountryId, CountryName, CountryStatus;

    public String getCountryId() {
        return CountryId;
    }
    public void setCountryId(String id) {
        CountryId = id;
    }

    public String getCountryName() {
        return CountryName;
    }
    public void setCountryName(String name) {
        CountryName = name;
    }

    public String getCountryStatus() {
        return CountryStatus;
    }
    public void setCountryStatus(String status) {
        CountryStatus = status;
    }

}
