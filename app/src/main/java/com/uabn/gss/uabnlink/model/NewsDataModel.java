package com.uabn.gss.uabnlink.model;

public class NewsDataModel {
    String UserImage, UserName, NewsDate, NewsDescription, NewsImg, LikeCount, CommentCount;

    public String getUserImage() {
        return UserImage;
    }
    public void setUserImage(String wing) {
        UserImage = wing;
    }

    public String getUserName() {
        return UserName;
    }
    public void setUserName(String wing) {
        UserName = wing;
    }

    public String getNewsDate() {
        return NewsDate;
    }
    public void setNewsDate(String wing) {
        NewsDate = wing;
    }

    public String getNewsDescription() {
        return NewsDescription;
    }
    public void setNewsDescription(String wing) { NewsDescription = wing; }

    public String getNewsImg() {
        return NewsImg;
    }
    public void setNewsImg(String wing) {
        NewsImg = wing;
    }

    public String getLikeCount() {
        return LikeCount;
    }
    public void setLikeCount(String wing) {
        LikeCount = wing;
    }

    public String getCommentCount() {
        return CommentCount;
    }
    public void setCommentCount(String wing) {
        CommentCount = wing;
    }
}
